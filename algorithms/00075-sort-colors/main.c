void sortColors(int* nums, int numsSize){
    int r=0,w=0,b=0,i;
    for(i=0;i<numsSize;i++){
        if(nums[i] == 0){
            r++;
        }else if(nums[i] == 1){
            w++;
        }else{
            b++;
        }
    }
    memcpy(&nums[0],red,sizeof(int) *r);
    memcpy(&nums[r],white,sizeof(int) *w);
    memcpy(&nums[r+w],blue,sizeof(int) *b);
}

